#include <unistd.h>

void	put_char(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	long	a;

	a = nb;
	if (a < 0)
	{
		put_char('-');
		a = -a;
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
		put_char(a % 10 + '0');
	}
	else
	{
		put_char(a % 10 + '0');
	}
}
