#include <unistd.h>

void	ft_putchar(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main(int argc, char **argv)
{
	argc = 1;
	ft_putchar(argv[0]);
	write(1, "\n", 1);
	return (0);
}
